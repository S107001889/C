/* prog3_5, 數字字元與其相對應的ASCII碼 */
#include <stdio.h>
#include <stdlib.h>
int main(void)
{
	printf("恩師好，我是王信凱\n");
   printf("我的C300題之14\n");
   printf("   \n");
   char ch='2';				/* 宣告字元變數ch，並設值為'2' */
   printf("ch=%c\n",ch);		/* 印出字元變數ch */
   printf("the ASCII of ch is %d\n",ch);  /* 印出ch的ASCII碼 */
   
   system("pause");
   return 0;
}


