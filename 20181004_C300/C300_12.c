/* prog3_3, 字元的列印*/
#include <stdio.h>
#include <stdlib.h>
int main(void)
{
	printf("恩師好，我是王信凱\n");
   printf("我的C300題之12\n");
   printf("   \n");
   char ch='a';				/* 宣告字元變數ch，並設值為'a' */
   printf("ch= %c\n",ch);		/* 印出ch的值 */ 
   printf("ASCII of ch= %d\n",ch);		/* 印出ch的十進位值 */
   
   system("pause");
   return 0;
}


